apple = 10  # 把10放到了自变量apple里面

print(apple) # print(apple) 就是打印 10

apple_egg = 20  #最好是加_把两个单词分开。

AppleEgg = 10 + 5 # 或者是 AppleEgg把两个单词分开

print(apple_egg)

print(AppleEgg)


